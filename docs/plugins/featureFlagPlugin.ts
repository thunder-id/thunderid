import type {Plugin, AllContent} from '@docusaurus/types';

interface DocFrontMatter {
  featureFlag?: string;
  [key: string]: unknown;
}

interface LoadedDoc {
  id: string;
  frontMatter: DocFrontMatter;
}

interface LoadedVersion {
  docs: LoadedDoc[];
}

interface LoadedContent {
  loadedVersions: LoadedVersion[];
}

/**
 * Reads the `featureFlag` frontmatter field from every doc and exposes a
 * `{ hiddenDocIds: string[] }` global data object containing doc IDs that
 * are gated behind a flag not present in the FEATURE_FLAGS environment variable.
 *
 * The sidebar component uses this list to suppress rendering of gated items
 * regardless of how the sidebar is configured (autogenerated or manual).
 */
export default function featureFlagPlugin(): Plugin {
  return {
    name: 'product-feature-flag-plugin',

    allContentLoaded({
      allContent,
      actions,
    }: {
      allContent: AllContent;
      actions: {setGlobalData: (data: unknown) => void};
    }) {
      const {setGlobalData} = actions;

      const enabledFlags = (process.env.FEATURE_FLAGS ?? '')
        .split(',')
        .map((f) => f.trim())
        .filter(Boolean);

      const docsContent = allContent['docusaurus-plugin-content-docs']?.default as LoadedContent | undefined;

      const hiddenDocIds: string[] = [];

      if (docsContent?.loadedVersions) {
        for (const version of docsContent.loadedVersions) {
          for (const doc of version.docs) {
            const flag = doc.frontMatter?.featureFlag;
            if (typeof flag === 'string' && flag.length > 0 && !enabledFlags.includes(flag)) {
              hiddenDocIds.push(doc.id);
            }
          }
        }
      }

      setGlobalData({hiddenDocIds});
    },
  };
}
