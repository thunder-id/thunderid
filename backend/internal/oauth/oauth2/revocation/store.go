// criterionType names a revocation criterion dimension in the REVOCATION_CRITERIA deny list.
type criterionType string

const (
	// criterionTypeTokenFamily revokes every token carrying a given token family id (tfid).
	criterionTypeTokenFamily criterionType = "token_family"
)

// revocationCriterion is a single criteria-based (many-token) revocation entry.
type revocationCriterion struct {
	// ID is the surrogate UUID v7 primary key. Generated by the store when empty.
	ID string
	// Type is the criterion dimension (e.g. token_family).
	Type criterionType
	// Value is the revoked value for the dimension (the tfid for token_family).
	Value string
	// Reason records why the criterion was revoked.
	Reason RevocationReason
	// RevokedAt is the time the revocation was recorded.
	RevokedAt time.Time
	// ExpiryTime bounds the entry; the row is removable once every token it could match has expired.
	ExpiryTime time.Time
}

// revocationStoreInterface defines the deny-list persistence for the revocation feature: the
// single-token deny list (InsertRevokedToken / IsTokenRevoked, by jti) written by the RFC 7009
// revocation service, and the criteria (many-token) deny list (insertCriterion / isCriterionRevoked,
// by criterion) written by the family revoker. Both read paths are consulted by the enforcement
// service on the AS hot path. The constructor is unexported so neither write path can be reached from
// outside the package, bypassing the revocation service's and revoker's validation and ownership.
type revocationStoreInterface interface {
	// InsertRevokedToken writes a JTI to the deny list. The write is idempotent.
	InsertRevokedToken(ctx context.Context, token RevokedToken) error
	// IsTokenRevoked reports whether a non-expired deny-list entry exists for the given JTI.
	IsTokenRevoked(ctx context.Context, jti string) (bool, error)
	// insertCriterion records a criteria-based revocation. The write is idempotent per
	// (deployment, type, value).
	insertCriterion(ctx context.Context, criterion revocationCriterion) error
	// isCriterionRevoked reports whether a non-expired criteria entry exists for the (type, value) pair.
	isCriterionRevoked(ctx context.Context, criterion criterionType, value string) (bool, error)
}

// revocationStore implements revocationStoreInterface against the runtime persistent database.
type revocationStore struct {
	dbProvider   provider.DBProviderInterface
	deploymentID string
}

// newRevocationStore creates a new revocationStore. It is intentionally unexported so the deny-list
// write paths cannot be reached from outside the package, bypassing the revocation service's and
// family revoker's validation and ownership checks.
func newRevocationStore() revocationStoreInterface {
	return &revocationStore{
		dbProvider:   provider.GetDBProvider(),
		deploymentID: config.GetServerRuntime().Config.Server.Identifier,
	}
}

// InsertRevokedToken writes a JTI to the deny list. A duplicate (deployment, jti) is a no-op.
// A UUID v7 surrogate primary key is generated when the token has no ID.
func (s *revocationStore) InsertRevokedToken(ctx context.Context, token RevokedToken) error {
	dbClient, err := s.dbProvider.GetRuntimePersistentDBClient()
	if err != nil {
		return fmt.Errorf("failed to get runtime persistent database client: %w", err)
	}

	id := token.ID
	if id == "" {
		id, err = utils.GenerateUUIDv7()
		if err != nil {
			return fmt.Errorf("failed to generate revoked token id: %w", err)
		}
	}

	_, err = dbClient.ExecuteContext(ctx, queryInsertRevokedToken, id, token.JTI,
		string(token.RevocationReason), token.RevokedAt, token.ExpiryTime, s.deploymentID)
	if err != nil {
		return fmt.Errorf("error inserting revoked token: %w", err)
	}

	return nil
}

// IsTokenRevoked reports whether a non-expired deny-list entry exists for the given JTI.
func (s *revocationStore) IsTokenRevoked(ctx context.Context, jti string) (bool, error) {
	dbClient, err := s.dbProvider.GetRuntimePersistentDBClient()
	if err != nil {
		return false, fmt.Errorf("failed to get runtime persistent database client: %w", err)
	}

	results, err := dbClient.QueryContext(ctx, queryIsTokenRevoked, jti, time.Now().UTC(), s.deploymentID)
	if err != nil {
		return false, fmt.Errorf("error checking token revocation: %w", err)
	}

	return len(results) > 0, nil
}

// insertCriterion records a criteria-based revocation. A duplicate (deployment, type, value) refreshes
// the reason and time bounds. A UUID v7 surrogate primary key is generated when none is supplied.
func (s *revocationStore) insertCriterion(ctx context.Context, criterion revocationCriterion) error {
	dbClient, err := s.dbProvider.GetRuntimePersistentDBClient()
	if err != nil {
		return fmt.Errorf("failed to get runtime persistent database client: %w", err)
	}

	id := criterion.ID
	if id == "" {
		id, err = utils.GenerateUUIDv7()
		if err != nil {
			return fmt.Errorf("failed to generate revocation criterion id: %w", err)
		}
	}

	_, err = dbClient.ExecuteContext(ctx, queryInsertRevocationCriterion, id, string(criterion.Type),
		criterion.Value, string(criterion.Reason), criterion.RevokedAt, criterion.ExpiryTime, s.deploymentID)
	if err != nil {
		return fmt.Errorf("error inserting revocation criterion: %w", err)
	}

	return nil
}

// isCriterionRevoked reports whether a non-expired criteria entry exists for the (type, value) pair.
func (s *revocationStore) isCriterionRevoked(ctx context.Context, criterion criterionType, value string) (
	bool, error) {
	dbClient, err := s.dbProvider.GetRuntimePersistentDBClient()
	if err != nil {
		return false, fmt.Errorf("failed to get runtime persistent database client: %w", err)
	}

	results, err := dbClient.QueryContext(ctx, queryIsCriterionRevoked, string(criterion), value,
		time.Now().UTC(), s.deploymentID)
	if err != nil {
		return false, fmt.Errorf("error checking revocation criterion: %w", err)
	}

	return len(results) > 0, nil
}
