#!/usr/bin/env node
// validate-exemptions.mjs
//
// Two jobs:
//  1. GOVERNANCE GATE — validate governance/exemptions.yaml against the schema,
//     and fail the build on any EXPIRED waiver. Warn on waivers expiring within
//     EXPIRY_WARN_DAYS. Report the active count.
//  2. EFFECTIVE RULESET — emit .spectral.effective.yaml = base ruleset + a
//     Spectral `overrides` block that turns each active waiver off for exactly
//     one operation. CI lints against this file, so a waiver cannot exist
//     without a schema-valid, non-expired, owned, tracked justification.
//
// Usage: node scripts/validate-exemptions.mjs
// Exit:  non-zero on schema violation or expired waiver.
//
// Deps: ajv, ajv-formats, js-yaml  (see package.json)

import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';
import yaml from 'js-yaml';
import Ajv2020 from 'ajv/dist/2020.js';
import addFormats from 'ajv-formats';

const ROOT = path.resolve(path.dirname(url.fileURLToPath(import.meta.url)), '..');
const SCHEMA = path.join(ROOT, 'governance', 'exemptions.schema.json');
const EXEMPTIONS = path.join(ROOT, 'governance', 'exemptions.yaml');
const BASE_RULESET = path.join(ROOT, '.spectral.yaml');
const EFFECTIVE_RULESET = path.join(ROOT, '.spectral.effective.yaml');
const EXPIRY_WARN_DAYS = 14;

const die = (msg) => {
  console.error(`\u2717 ${msg}`);
  process.exit(1);
};

const today = new Date();
today.setHours(0, 0, 0, 0);

// --- load + schema-validate -------------------------------------------------
const schema = JSON.parse(fs.readFileSync(SCHEMA, 'utf8'));
const doc = yaml.load(fs.readFileSync(EXEMPTIONS, 'utf8')) || { version: 1, exemptions: [] };

const ajv = new Ajv2020({ allErrors: true, strict: false });
addFormats(ajv);
const validate = ajv.compile(schema);
if (!validate(doc)) {
  console.error('Exemptions file failed schema validation:');
  for (const e of validate.errors) console.error(`  ${e.instancePath || '/'} ${e.message}`);
  process.exit(1);
}

// --- expiry gate ------------------------------------------------------------
const expired = [];
const expiringSoon = [];
for (const ex of doc.exemptions) {
  const exp = new Date(`${ex.expires}T00:00:00`);
  const days = Math.ceil((exp - today) / 86400000);
  if (days < 0) expired.push({ ex, days });
  else if (days <= EXPIRY_WARN_DAYS) expiringSoon.push({ ex, days });
}

if (expired.length) {
  console.error('Expired exemptions (renew with a fresh expiry+issue, or fix the endpoint):');
  for (const { ex, days } of expired) {
    console.error(`  ${ex.rule} @ ${ex.file}#${ex.pointer} — expired ${-days} day(s) ago, owner ${ex.owner}, ${ex.issue}`);
  }
  die(`${expired.length} expired exemption(s).`);
}

for (const { ex, days } of expiringSoon) {
  console.warn(`\u26a0 ${ex.rule} @ ${ex.file}#${ex.pointer} expires in ${days} day(s) (owner ${ex.owner}).`);
}

// --- emit effective ruleset -------------------------------------------------
const base = yaml.load(fs.readFileSync(BASE_RULESET, 'utf8'));
base.overrides = doc.exemptions.map((ex) => ({
  files: [`${ex.file}#${ex.pointer}`],
  rules: { [ex.rule]: 'off' },
}));

const banner =
  '# GENERATED by scripts/validate-exemptions.mjs — do not edit by hand.\n' +
  '# Edit .spectral.yaml (rules) or governance/exemptions.yaml (waivers) instead.\n';
fs.writeFileSync(EFFECTIVE_RULESET, banner + yaml.dump(base, { lineWidth: 100 }));

console.log(
  `\u2713 ${doc.exemptions.length} active exemption(s), 0 expired. Wrote ${path.relative(ROOT, EFFECTIVE_RULESET)}.`
);
