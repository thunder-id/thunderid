import WelcomeStorageKeys from '../constants/storage-keys';

/**
 * Generates the storage key for tracking the configured state of the Wayfinder setup section in the welcome page.
 * The key is generated by replacing the `{{productName}}` placeholder in the `WelcomeStorageKeys.WAYFINDER_CONFIGURED`
 * template with the lowercase version of the provided product name.
 * @param productName
 * @returns {string} The storage key for tracking the Wayfinder setup section configured state.
 */
export default function getWayfinderConfiguredStorageKey(productName: string): string {
  return WelcomeStorageKeys.WAYFINDER_CONFIGURED.replace('{{productName}}', productName.toLowerCase());
}
